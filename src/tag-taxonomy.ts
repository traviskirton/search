/**
 * Tag Taxonomy
 *
 * Defines a 2-level parent > child hierarchy for tags.
 */

/** Convert kebab-case tag to plain language */
export function tagToLabel(tag: string): string {
  return tag
    .split('-')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ')
}

