/** Get category by ID */
export function getCategory(id: string): TagCategory | undefined {
  return TAG_TAXONOMY.find(c => c.id === id)
}

/** Get all category IDs */
export function getCategoryIds(): string[] {
  return TAG_TAXONOMY.map(c => c.id)
}
